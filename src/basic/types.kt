package basic

fun main(args: Array<String>) {
    // inteiros
    val num1: Byte = 127
    val num2: Short = 32666
    val num3: Long = 1121423343434343443
    val num4: Int = 444444444

    // ponto flutuante
    val num5: Double = 2.5543546
    val num6: Float = 3.14f

    // caracteres
    val char: Char = '?'

    // booleano
    val boolean: Boolean = true

    println(listOf(num1, num2, num3, num4, num5, num6, char, boolean))
}

